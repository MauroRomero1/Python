
# ----------- Métodos de Listas -----------

# append(x): Agrega un elemento al final de la lista.
# clear(): Elimina todos los elementos de la lista.
# copy(): Devuelve una copia de la lista.
# count(x): Devuelve el número de veces que aparece un elemento.
# extend(iterable): Extiende la lista agregando elementos de un iterable.
# index(x[, start[, end]]): Devuelve el índice de la primera aparición de un elemento.
# insert(i, x): Inserta un elemento en la posición indicada.
# pop([i]): Elimina y devuelve el elemento en la posición indicada (por defecto, el último).
# remove(x): Elimina la primera aparición de un elemento.
# reverse(): Invierte el orden de los elementos en la lista.
# sort(key=None, reverse=False): Ordena los elementos de la lista (puede usar una clave personalizada).
