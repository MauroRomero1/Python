"""Qué es:
    Django es un framework completo y de alto nivel diseñado para facilitar el desarrollo rápido de aplicaciones web. Ofrece una gran cantidad de herramientas integradas para manejar tareas comunes como la conexión a bases de datos, la creación de APIs, la autenticación de usuarios, y más.
Características:
    "Baterías incluidas": Tiene módulos preconfigurados para casi todo, como formularios, seguridad, sesiones, y administración.
    Basado en el patrón MTV (Model-Template-View):
        Modelos: Definen y gestionan los datos y sus relaciones.
        Templates: Renderizan contenido dinámico en HTML.
        Views: Procesan solicitudes y retornan respuestas.
    Administración automática: Genera un panel de administración web para manejar modelos con facilidad.
    Seguridad: Protección contra XSS, CSRF, SQL Injection y más.
Cuándo usarlo:
Ideal para proyectos grandes o aplicaciones complejas donde necesitas mucho soporte y herramientas integradas.
"""