# clear(): Elimina todos los elementos del diccionario.
# copy(): Devuelve una copia del diccionario.
# fromkeys(): Crea un nuevo diccionario con claves del iterable y valores predeterminados.
# get(): Devuelve el valor de una clave o un valor predeterminado si no existe.
# items(): Devuelve una vista de pares clave-valor.
# keys(): Devuelve una vista de las claves.
# pop(): Elimina una clave y devuelve su valor, o un valor predeterminado.
# popitem(): Elimina y devuelve un par clave-valor arbitrario.
# setdefault(): Devuelve el valor de una clave, si no existe, inserta la clave con un valor predeterminado.
# update(): Actualiza el diccionario con pares clave-valor de otro diccionario o iterable.
# values(): Devuelve una vista de los valores.
