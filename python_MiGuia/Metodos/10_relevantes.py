# ----- Otros Métodos Relevantes -----

# dir(object): Devuelve una lista de atributos y métodos del objeto.
# help(object): Muestra la documentación de un objeto o función.
# id(object): Devuelve el identificador único de un objeto.
# type(object): Devuelve el tipo del objeto.
# isinstance(object, classinfo): Verifica si un objeto es una instancia de una clase dada.
# hasattr(object, name): Verifica si un objeto tiene un atributo con el nombre dado.
# getattr(object, name[, default]): Devuelve el valor de un atributo del objeto.
# setattr(object, name, value): Establece el valor de un atributo en el objeto.
# delattr(object, name): Elimina un atributo del objeto.
