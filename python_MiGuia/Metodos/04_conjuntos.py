# --------- Métodos de Conjuntos -----------
# add(): Agrega un elemento al conjunto.
# clear(): Elimina todos los elementos del conjunto.
# copy(): Devuelve una copia del conjunto.
# difference(): Devuelve la diferencia entre conjuntos.
# difference_update(): Elimina los elementos comunes con otros conjuntos.
# discard(): Elimina un elemento si está presente (no lanza error si no está).
# intersection(): Devuelve la intersección con otros conjuntos.
# intersection_update(): Actualiza el conjunto con la intersección de otros.
# isdisjoint(): Verifica si no hay elementos comunes con otro conjunto.
# issubset(): Verifica si el conjunto es un subconjunto de otro.
# issuperset(): Verifica si el conjunto es un superconjunto de otro.
# pop(): Elimina y devuelve un elemento arbitrario del conjunto.
# remove(): Elimina un elemento del conjunto (lanza error si no existe).
# symmetric_difference(): Devuelve los elementos únicos de cada conjunto.
# symmetric_difference_update(): Actualiza con la diferencia simétrica.
# union(): Devuelve la unión de conjuntos.
# update(): Agrega elementos de otros conjuntos al conjunto actual.
