# Métodos Generales de Iterables (Comunes a Varios Tipos)

# all(iterable): Devuelve True si todos los elementos son verdaderos.
# any(iterable): Devuelve True si algún elemento es verdadero.
# enumerate(iterable, start=0): Devuelve un objeto enumerado (índice, valor) de un iterable.
# filter(function, iterable): Devuelve los elementos de un iterable que cumplen con la función.
# map(function, iterable): Aplica una función a cada elemento de un iterable.
# zip(*iterables): Combina elementos de múltiples iterables en tuplas.