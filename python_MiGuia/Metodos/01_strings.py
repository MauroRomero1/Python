# ----- Métodos de Cadenas (Strings) -----

# capitalize(): Convierte la primera letra a mayúscula.
# casefold(): Convierte toda la cadena a minúsculas (más agresivo que lower()).
# center(width): Centra la cadena en un ancho especificado.
# count(sub[, start[, end]]): Cuenta cuántas veces aparece un substring en la cadena.
# encode(encoding='utf-8', errors='strict'): Devuelve una versión codificada de la cadena.
# endswith(suffix[, start[, end]]): Verifica si la cadena termina con el sufijo dado.
# find(sub[, start[, end]]): Devuelve el índice de la primera aparición de un substring o -1.
# format(*args, **kwargs): Formatea la cadena usando marcadores.
# index(sub[, start[, end]]): Devuelve el índice de la primera aparición de un substring o lanza ValueError si no lo encuentra.
# isalnum(): Verifica si todos los caracteres son alfanuméricos.
# isalpha(): Verifica si todos los caracteres son letras.
# isdigit(): Verifica si todos los caracteres son dígitos.
# islower(): Verifica si todos los caracteres están en minúscula.
# isspace(): Verifica si la cadena contiene solo espacios.
# istitle(): Verifica si la cadena está en formato título (cada palabra capitalizada).
# isupper(): Verifica si todos los caracteres están en mayúscula.
# join(iterable): Une un iterable con la cadena como separador.
# lower(): Convierte todos los caracteres a minúsculas.
# lstrip([chars]): Elimina caracteres desde el inicio de la cadena.
# partition(sep): Divide la cadena en tres partes usando un separador.
# replace(old, new[, count]): Reemplaza subcadenas por otra, opcionalmente limitando el número de reemplazos.
# rfind(sub[, start[, end]]): Devuelve el índice de la última aparición de un substring o -1.
# rstrip([chars]): Elimina caracteres desde el final de la cadena.
# split(sep=None, maxsplit=-1): Divide la cadena en una lista usando un separador.
# splitlines([keepends]): Divide la cadena en una lista de líneas.
# startswith(prefix[, start[, end]]): Verifica si la cadena comienza con un prefijo dado.
# strip([chars]): Elimina caracteres desde ambos extremos de la cadena.
# swapcase(): Cambia mayúsculas a minúsculas y viceversa.
# title(): Convierte la cadena a formato título (cada palabra capitalizada).
# upper(): Convierte todos los caracteres a mayúsculas.
# zfill(width): Rellena la cadena con ceros a la izquierda para alcanzar el ancho especificado.

