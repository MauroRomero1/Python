# ------ Métodos de Archivos ---------

# close(): Cierra el archivo.
# flush(): Vacía el búfer de escritura.
# read(): Lee el contenido del archivo.
# readline(): Lee una línea del archivo.
# readlines(): Lee todas las líneas y las devuelve como una lista.
# seek(): Cambia la posición del puntero en el archivo.
# tell(): Devuelve la posición actual del puntero en el archivo.
# write(): Escribe una cadena en el archivo.
# writelines(): Escribe una lista de cadenas en el archivo.
